    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
       switch(jTabbedPane1.getSelectedIndex()){
            case 0:{
                  this.puntox.setText("");
                  this.puntoy.setText("");
                  this.jTAPunto.setText("");
                  this.jTALista.setText("");
                  lienzo.repaint();
                  break;
                  
            }
            
            case 2:{
                
                  this.ax.setText("");
                  this.ay.setText("");
                  this.ara.setText("");
                  this.af.setText("");
                  this.jTALista.setText("");
                  lienzo.repaint();
                  break;
                }
            
            case 1:{
                  this.cilindrox.setText("");
                  this.cilindroy.setText("");
                  this.cilindroradio.setText("");
                  this.cilindroaltura.setText("");
                  this.jTACilindro.setText("");
                  this.jTALista.setText("");
                  lienzo.repaint();
                  break;
            }
            default:  
                break;
          }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

