    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
       Graphics g=lienzo.getGraphics();  
       switch(jTabbedPane1.getSelectedIndex()){
            case 0:{
                 lienzo.paint(g); 
                  x=Integer.parseInt(this.puntox.getText());
                  y=Integer.parseInt(this.puntoy.getText());
                  punto=new Punto(x, y);
                  this.jTAPunto.setText(punto.toString());
                  lista.addFigura(punto);
                  this.jTALista.setText(lista.toString());
                  g.setColor(Color.blue);
                  g.fillOval(x, y, 5, 5);
                  break; }
            
            case 2:{
                 lienzo.paint(g); 
                  x=Integer.parseInt (this.ax.getText()) ;
                  y=Integer.parseInt (this.ay.getText()) ;
                  radio=Integer.parseInt (this.ara.getText()) ;
                  circulo=new Circulo(radio,x, y);
                  this.af.setText(circulo.toString());
                  lista.addFigura(circulo);
                  this.jTALista.setText(lista.toString());
                  int diametro = radio*2;
                  g.setColor(Color.blue);
                  g.drawOval(x, y, diametro, diametro);
                   break;  }
            
            case 1:{
                  lienzo.paint(g); 
                  x=Integer.parseInt(this.cilindrox.getText());
                  y=Integer.parseInt(this.cilindroy.getText());
                  radio=Integer.parseInt(this.cilindroradio.getText());
                  altura=Integer.parseInt(this.cilindroaltura.getText());
                  cilindro=new Cilindro(altura,radio, x, y);
                  this.jTACilindro.setText(cilindro.toString());
                  lista.addFigura(cilindro);
                  this.jTALista.setText(lista.toString());
                  int diametro = radio*2;
                  g.setColor(Color.blue);
                  g.drawOval(x, y, diametro, radio);
                  g.drawLine(x, y+(radio/2), x, y+altura+(radio/2));
                  g.drawLine(x+diametro,y+(radio/2),x+diametro,y+altura+(radio/2));
                  g.drawOval(x, y+altura, diametro, radio);
                  break;
            }
            
          }
      
    }//GEN-LAST:event_jButton1ActionPerformed

